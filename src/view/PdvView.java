    private void salvarVenda() {
        int cont = 0;
        int codigoProduto = 0;
        int codigoVenda = 0;

        modelVendas = new ModelVendas();
        modelVendas.setCliente(1);
        try {
            modelVendas.setVenDataVenda(blDatas.converterDataParaDateUS(new Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            Logger.getLogger(PdvView.class.getName()).log(Level.SEVERE, null, ex);
        }
        modelVendas.setVenValorBruto(Double.parseDouble(jtfValorBruto.getText()));
        /*
         provavel criação de frame para desconto. Alterar de 0 para a nova regra de negocio
         */
        modelVendas.setVenDesconto(viewPagamentoPDV.getDesconto());
        /**
         * se um frame novo for criado para o desconto, aqui ficara esse
         * jtfValorTotal - desconto
         */
        modelVendas.setVenValorLiquido(viewPagamentoPDV.getValorTotal());
        modelVendas.setVenFormaPagamento(viewPagamentoPDV.getPagamento());
        modelVendas.setVenParcelas(viewPagamentoPDV.getParcelasPagamento());
        codigoVenda = controllerVendas.salvarVendasController(modelVendas);

        cont = jtProdutos.getRowCount();
        for (int i = 0; i < cont; i++) {
            modelProdutos = new ModelProdutos();
            modelVendasProdutos = new ModelVendasProdutos();
            codigoProduto = (int) jtProdutos.getValueAt(i, 1);
            modelVendasProdutos = new ModelVendasProdutos();
            modelProdutos = new ModelProdutos();
            modelVendasProdutos.setProduto(codigoProduto);
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVendasProValor((double) jtProdutos.getValueAt(i, 4));
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jtProdutos.getValueAt(i, 3).toString()));

            //produto
            modelProdutos.setIdProduto(codigoProduto);
            modelProdutos.setProEstoque(controllerProduto.retornarProdutoController(codigoProduto).getProEstoque()
                    - Integer.parseInt(jtProdutos.getValueAt(i, 3).toString()));
            listaModelVendasProdutos.add(modelVendasProdutos);
            listaModelProdutos.add(modelProdutos);
        }
        if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)) {
            controllerProduto.alterarEstoqueProdutosController(listaModelProdutos);
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar os produtos da venda!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void limparCampos() {
        jtfValorBruto.setText("");
        DefaultTableModel modelo = (DefaultTableModel) jtProdutos.getModel();
        modelo.setNumRows(0);
        jlStatus.setText("Livre");
        jlStatus.setForeground(Color.green);
    }

