    private void pegarConteudo(java.awt.event.KeyEvent evt) {
        jlStatus.setText("Ocupado");
        jlStatus.setForeground(Color.red);
        DefaultTableModel modelo = (DefaultTableModel) jtProdutos.getModel();
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            try {
                modelProdutos = controllerProduto.retornarProdutoController(Integer.parseInt(jtfCodigo.getText()));
                modelo.addRow(new Object[]{
                    modelo.getRowCount() + 1,
                    modelProdutos.getIdProduto(),
                    modelProdutos.getProNome() + " " + modelProdutos.getProDescricao(),
                    quantidade,
                    modelProdutos.getProValorVenda(),
                    modelProdutos.getProValorVenda() * quantidade
                });
                jtfValorBruto.setText(String.valueOf(somaValorTotal()));
                jtfCodigo.setText("");
                quantidade = 1;
            } catch (Exception e) {
                //JOptionPane.showMessageDialog(this, "Informe neste campo apenas números", "Erro", JOptionPane.ERROR_MESSAGE);
                jtfCodigo.setText("");
            }

        }
    }

    private float somaValorTotal() {
        float soma = 0, valor = 0;
        int cont = jtProdutos.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(jtProdutos.getValueAt(i, 5)));
            soma += valor;
        }
        return soma;
    }

